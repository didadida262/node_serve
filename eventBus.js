class Bus {
  constructor() {
    this.resp = []
  }
  $on(name, fn) {
    this.resp[name] = this.resp[name] || []
    this.resp[name].push(fn)
  }
  $emit(name, data) {
    this.resp[name].forEach(watcher => {
      watcher(data)
    })
  }
  $off(name) {
    if (this.resp[name]) {
      delete this.resp[name]
    }
  }
  
}